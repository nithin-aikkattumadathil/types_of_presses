void pwm_led(void)
    {
    	for(int x=0; x<1000; x=x+1)
		{
		  __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2, x);
		  HAL_Delay(2);
		}
		for(int x=1000; x>0; x=x-1)
		{
		  __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2, x);
		  HAL_Delay(2);
		}
    }

